    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        // TODO add your handling code here:
        txtALocation.setForeground(Color.black);
        txtDLocation.setForeground(Color.black);
        txtDate.setForeground(Color.black);
        txtNum.setForeground(Color.black); 
        
        if(NumPattern() == false){
        JOptionPane.showMessageDialog(null, "Flight number should be composed of numbers!!", "Warning", JOptionPane.WARNING_MESSAGE);
        txtNum.setForeground(Color.red);}
             
        else if(DLocationPattern() == false){
        JOptionPane.showMessageDialog(null, "Departure Location should be composed of letters!!", "Warning", JOptionPane.WARNING_MESSAGE);
        txtDLocation.setForeground(Color.red);}
        
        else if(ALocationPattern() == false){
        JOptionPane.showMessageDialog(null, "Arrival Location should be composed of letters!!", "Warning", JOptionPane.WARNING_MESSAGE);
        txtALocation.setForeground(Color.red);}
        
        else if(DatePattern() == false){
        JOptionPane.showMessageDialog(null, "Date should be in the formation of dd/mm/yyyy", "Warning", JOptionPane.WARNING_MESSAGE);
        txtDate.setForeground(Color.red);
        }
        
        else{
        Flight flight =  airplane.getFlightCatalog().addFlight();
        flight.setFlightNumber(txtNum.getText());
        flight.setDateofDay(txtDate.getText());
        flight.setDepartureLocation(txtDLocation.getText());
        flight.setArrivalLocation(txtALocation.getText());
        flight.setTime(jComboBox1.getSelectedItem().toString());
        
        JOptionPane.showMessageDialog(null, "Airplane added successfully!!", "Info", JOptionPane.INFORMATION_MESSAGE);
        txtNum.setText("");
        txtDate.setText("");
        txtALocation.setText("");
        txtDLocation.setText("");
        }
    }//GEN-LAST:event_btnCreateActionPerformed

