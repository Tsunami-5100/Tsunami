        private boolean NumPattern(){
        Pattern p = Pattern.compile("[0-9]+");
        Matcher m = p.matcher(txtNum.getText());
        boolean b = m.matches();
        return b;
    }
        
        private boolean DLocationPattern(){
        Pattern p = Pattern.compile("[a-zA-Z]+");
        Matcher m = p.matcher(txtDLocation.getText());
        boolean b = m.matches();
        return b;
    }
        
        private boolean ALocationPattern(){
        Pattern p = Pattern.compile("[a-zA-Z]+");
        Matcher m = p.matcher(txtALocation.getText());
        boolean b = m.matches();
        return b;
    }
        
        private boolean DatePattern(){
        Pattern p = Pattern.compile("[0-9]+/[0-9]+/[0-9]+$");
        Matcher m = p.matcher(txtDate.getText());
        boolean b = m.matches();
        return b;
    }
 
