    public void refreshTable(){
        DefaultTableModel model = (DefaultTableModel) AirplaneCatalogTable.getModel();
        model.setRowCount(0);
        for(Airplane a: airliner.getAirplaneCatalog().getAirplaneCatalog()){
            Object row[] = new Object[3];
            row[0] = a;
            row[1] = a.getModel();
            row[2] = a.getManufacturedYear();
            model.addRow(row);
        }
    }
