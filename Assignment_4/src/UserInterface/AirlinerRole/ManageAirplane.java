    private void btnViewPlaneActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewPlaneActionPerformed
        // TODO add your handling code here:
        int row = AirplaneCatalogTable.getSelectedRow();
        if(row<0){
            JOptionPane.showMessageDialog(null, "Please select a row!!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        Airplane a = (Airplane)AirplaneCatalogTable.getValueAt(row,0);
        ManageFlight ma = new ManageFlight(Container,a,flightCatalog);
        Container.add("ManageFlight", ma);
        CardLayout layout = (CardLayout) Container.getLayout();
        layout.next(Container);
    }//GEN-LAST:event_btnViewPlaneActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int row = AirplaneCatalogTable.getSelectedRow();
        if(row<0){
            JOptionPane.showMessageDialog(null, "Please select a row!!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        Airplane s = (Airplane) AirplaneCatalogTable.getValueAt(row, 0);
        airplaneCatalog.removeAirplane(s);
        refreshTable();
    }//GEN-LAST:event_btnDeleteActionPerformed

