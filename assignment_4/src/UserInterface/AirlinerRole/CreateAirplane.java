        private boolean modelPattern(){
        Pattern p = Pattern.compile("[a-zA-Z]+-[0-9]+");
        Matcher m = p.matcher(txtModel.getText());
        boolean b = m.matches();
        return b;
    }
 
        
        private boolean IDPattern(){
        Pattern p = Pattern.compile("[0-9]+");
        Matcher m = p.matcher(txtId.getText());
        boolean b = m.matches();
        return b;
    }
        
        private boolean MYear(){
        Pattern p = Pattern.compile("[0-9]+");
        Matcher m = p.matcher(txtYear.getText());
        boolean b = m.matches();
        return b;
        }
        
