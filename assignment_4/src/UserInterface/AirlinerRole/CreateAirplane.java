    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        // TODO add your handling code here:
               txtModel.setForeground(Color.black);
               txtId.setForeground(Color.black);
               txtYear.setForeground(Color.black);
               
               if(modelPattern() == false){
               JOptionPane.showMessageDialog(null, "Modle name should be in the formation of xx(letters)-xx(numbers)!!", "Info", JOptionPane.WARNING_MESSAGE);
               txtModel.setForeground(Color.red);}
             
               else if(IDPattern() == false){
               JOptionPane.showMessageDialog(null, "Modle ID should be composed of numbers!!", "Info", JOptionPane.WARNING_MESSAGE);
               txtId.setForeground(Color.red);}
               
               else if(MYear() == false){
               JOptionPane.showMessageDialog(null, "Manufactured Year should be composed of numbers", "Info", JOptionPane.WARNING_MESSAGE);
               txtYear.setForeground(Color.red);
               }
               
               else{
               Airplane airplane = fleet.getAirplaneCatalog().addAirplane();
               airplane.setId(txtId.getText());
               airplane.setModel(txtModel.getText());
               airplane.setManufacturedYear(txtYear.getText());
//               airplane.setSeating();
        
               JOptionPane.showMessageDialog(null, "Airplane added successfully!!", "Info", JOptionPane.INFORMATION_MESSAGE);
               
               txtId.setText("");
               txtModel.setText("");
               txtYear.setText("");
               }
           
                
            
       
    }//GEN-LAST:event_btnCreateActionPerformed

