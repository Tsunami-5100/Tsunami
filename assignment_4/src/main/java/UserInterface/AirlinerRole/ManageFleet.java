    public void refreshProductTable(String keyword){
           DefaultTableModel model = (DefaultTableModel)tableFleet.getModel();
           model.setRowCount(0);

               for(Fleet a : airliner.getFleetCatalog().getFleetList()){
                   if( a.getFleetId() == Integer.parseInt(txtFleet.getText())){
                        Object row[] = new Object[2];
                        row[0] = a;
                        row[1] = a.getAirplaneCatalog().getAirplaneCatalog().size();
                        model.addRow(row);
                    }
               }
           }
    
    public void refreshTable(){

        DefaultTableModel model = (DefaultTableModel) tableFleet.getModel();
        model.setRowCount(0);


        for (Fleet f : airliner.getFleetCatalog().getFleetList()) {
            
            Object row[] = new Object[2];
            row[0] = f;
            row[1] = f.getAirplaneCatalog().getAirplaneCatalog().size();
            model.addRow(row);        }
    }
    
           
