    private void btnViewPlaneActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewPlaneActionPerformed
        // TODO add your handling code here:
        int row = tableFleet.getSelectedRow();
        if(row<0){
            JOptionPane.showMessageDialog(null, "Please select a row!!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        Fleet s = (Fleet)tableFleet.getValueAt(row,0);
        
        ManageAirplane ma = new ManageAirplane(Container, s,apc);
        Container.add("ManageAirplane", ma);
        CardLayout layout = (CardLayout) Container.getLayout();
        layout.next(Container);
     
    }//GEN-LAST:event_btnViewPlaneActionPerformed

    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed

        int selectedRow = tableFleet.getSelectedRow();
        if(selectedRow>=0) {
            int selectionButton = JOptionPane.YES_NO_OPTION;
            int selectionResult = JOptionPane.showConfirmDialog(null, "Are you sure to remove??","Warning",selectionButton);
            if(selectionResult == JOptionPane.YES_OPTION){
                Fleet f = (Fleet) tableFleet.getValueAt(selectedRow, 0);
                airliner.getFleetCatalog().removeFleet(f);
                refreshTable();
            }
        }else{
            JOptionPane.showMessageDialog(null, "Please select a Row!!");
        }
            
    }//GEN-LAST:event_btnRemoveActionPerformed

