   public void refreshTable(){
        DefaultTableModel model = (DefaultTableModel) FlightCatalogTable.getModel();
        model.setRowCount(0);
        for(Ticket t : flight.getTicketCatalog().getTicketCatalog()){
            Object row[] = new Object[4];
            row[0] = t;
            row[1] = t.getLocation();
            row[2] = t.getPrice();
            row[3] = t.getOrderStaus();
            model.addRow(row);
        }
    }
   
   
