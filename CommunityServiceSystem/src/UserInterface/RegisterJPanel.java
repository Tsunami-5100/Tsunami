    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        // TODO add your handling code here:
        String fullname = txtFullName.getText();
        String ssn = txtSSN.getText();
        String email = TXTEmail.getText();
        String phoneNum = txtPhoneNum.getText();
        String username = txtUserName.getText();
        String password = txtPassword.getText();
        String networkName = RComboNetwork.getSelectedItem().toString();
        String repassword = String.valueOf(fieldRePassword.getPassword());
        if (repassword.equals(password)) {
            Employee employee = new Employee();
            employee.setId(ecoSystem.getResidentDirectory().getResidentAccountList().size()+1);
            employee.setName(fullname);
            if (ecoSystem.getResidentDirectory() == null){
                ecoSystem.setResidentDirectory(new ResidentDirectory());
            }
            ecoSystem.getResidentDirectory().createResidentAccount(username, password, employee, new ResidentRole());
            ecoSystem.getResidentDirectory().addResident(employee.getId(), networkName, fullname, ssn, email, phoneNum);
            
            JOptionPane.showMessageDialog(null, "Account created successfully!");
            dB4OUtil.storeSystem(ecoSystem);
            txtFullName.setText("");
            txtSSN.setText("");
            TXTEmail.setText("");
            txtPhoneNum.setText("");
            txtUserName.setText("");
            txtPassword.setText("");
            fieldRePassword.setText("");
        }
        
        else {
            JOptionPane.showMessageDialog(null, "Your Password and Re-password should be consistent!");
        }
    }//GEN-LAST:event_btnRegisterActionPerformed

